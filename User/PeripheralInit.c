#include <stm32f10x.h>
#include "PeripheralInit.h"
#include "SPFD5408.h"
#include "SPI.h"
#include "key_4_4.h"

//-----------------------------------------------------------------
// 初始化程序区
//-----------------------------------------------------------------
//-----------------------------------------------------------------
// void PeripheralInit(void)
//-----------------------------------------------------------------
// 
// 函数功能: 系统外设初始化
// 入口参数: 无 
// 返 回 值: 无
// 全局变量: 无
// 调用模块: 无     
// 注意事项: 无
    
//-----------------------------------------------------------------
void PeripheralInit(void)
{
	SPFD5408_Init();													// 液晶初始化
	SPFD5408_Clear_Window(WHITE);							// 清屏白色							
	SPI_Configuration(SPI_2);									// MCU与FPGA的SPI通信初始化	
  GPIO_Keyboard_Configuration();						// 键盘初始化	
}

